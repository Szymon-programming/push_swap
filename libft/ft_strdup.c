#include "libft.h"

char	*ft_strdup(const char *s)
{
	char				*destination;
	size_t				len;

	len = ft_strlen(s) + 1;
	destination = malloc(len);
	if (destination == NULL)
		return (NULL);
	ft_strlcpy(destination, s, len);
	return (destination);
}
