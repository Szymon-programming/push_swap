#include "libft.h"

void	*ft_memchr(const void *s, int c, size_t n)
{
	const unsigned char	*source;
	unsigned char		wanted;

	source = (const unsigned char *)s;
	wanted = (unsigned char)c;
	while (n > 0)
	{
		if (*source == wanted)
		{
			return ((void *)source);
		}
		source++;
		n--;
	}
	return (NULL);
}
